	        <section>
	        
	        	<h4 class="section"> 2. Fastenings</h4>
	        	
	        	<p>The mask is currently inside-out, so the fastenings are going to be attached on the inside. It's difficult to describe the process but there are diagrams!</p>
	        	
	        	<h6 class="subsection">Option A, Ear loops</h6>
	        	
	        	<p>Each ear loop is going in between the layers of fabric, and with just a small amount sticking out at the top and bottom where it will be attached.</p>
	        	
        		<figure><a href="/images/creative/Ear loops diagram.JPG" class="lightbox" title="Diagram showing ear loops positioned between layers of fabric"><img class="embedded" src="/images/creative/Ear loops diagram.JPG"/></a></figure>
	        	
	        	<p>Slide one piece of elastic in through the opening at the bottom. Then push one end of it out the side, near the top corner. Leave just a couple of millimetres space from the top edge, and about 5mm of the elastic extending out of the side, then pin in place.</p>
	        	
        		<figure><a href="/images/creative/IMG_5194.JPG" class="lightbox" title="End of elastic sticking out between fabric layers"><img class="embedded" src="/images/creative/IMG_5194.JPG"/></a></figure>
	        	
	        	<p>Grab the other end of the same elastic, and push it out of the side near the bottom. Try to make sure that the elastic is flat and not twisted, it will make for a neater and more comfortable ear loop. </p>
	        	
        		<figure><a href="/images/creative/Ear loop shape.png" class="lightbox" title="Comparison of flat and twisted elastic loops"><img class="embedded" src="/images/creative/Ear loop shape.png"/></a></figure>
	        	
	        	<p>The fabric will crumple because the elastic is under tension, but that's okay as long as the ends of the elastic are in the right places. Again leave 5mm of the elastic extending out of the side, but this time leave about 15mm from the bottom edge (you're leaving space for a seam), then pin in place.</p>
	        	
	        	<figure><a href="/images/creative/IMG_5195.JPG" class="lightbox" title="End of elastic sticking out between crumpled fabric layers"><img class="embedded" src="/images/creative/IMG_5195.JPG"/></a></figure>
	        	
	        	<p>Repeat the same operation with the second elastic on the other side. When you're done you'll have a horrifying crumpled mess like this (don't worry, it will make sense again later):</p>
	        	
	        	<figure><a href="/images/creative/IMG_5198.JPG" class="lightbox" title="Crumpled pinned fabric"><img class="embedded" src="/images/creative/IMG_5198.JPG"/></a></figure>
	        
	        	<h6 class="subsection">Option B, Head straps</h6>
	        	
	        	<p> Each head strap is going in between the layers of fabric, and with just a small amount sticking out at either side where it will be attached.</p>
	        	
	        	<figure><a href="/images/creative/Head straps diagram.jpg" class="lightbox" title="Diagram showing head straps positioned between layers of fabric"><img class="embedded" src="/images/creative/Head straps diagram.jpg"/></a></figure>
	        
	        	<p>Slide one strap in through the opening at the bottom. Then push one end of it out of the side, near the top corner. Leave just a couple of millimetres from the top edge, and about 5mm of the strap extending out of the side, then pin in place.</p>
	        	
	        	<figure><a href="/images/creative/IMG_5211.JPG" class="lightbox" title="End of strap sticking out between fabric layers"><img class="embedded" src="/images/creative/IMG_5211.JPG"/></a></figure>
	        
	        	<p>Grab the other end of the strap, and push it out of the other side near the top in the same way, and pin in place. Make sure that the strap is flat and not twisted, otherwise it will be difficult to wear. The middle of the strap will be bunched up inside, that's okay as long as the ends are in the right places.</p>
	        	
	        	<figure><a href="/images/creative/Head strap shape.png" class="lightbox" title="Comparison of flat, twisted, and reversed straps"><img class="embedded" src="/images/creative/Head strap shape.png"/></a></figure>
	        
	        	<p>Slide the next strap inside and push one end out of the side near the bottom. Try to make sure the two straps are the same way up and not flipped, otherwise it will be difficult to adjust. Again leave 5mm of the strap sticking out past the side, but this time leave about 15mm from the bottom edge (you're leaving space for a seam), and pin in place.</p>
	        	
	        	<figure><a href="/images/creative/IMG_5214.JPG" class="lightbox" title="End of strap sticking out between bottom fabric layers"><img class="embedded" src="/images/creative/IMG_5214.JPG"/></a></figure>
	        	
	        	<p>Repeat with the other end of the strap on the other side, making sure not to twist it. When you're done you'll have a lumpy mess like this (don't worry, it will make sense again later):</p>
	        	
	        	<figure><a href="/images/creative/IMG_5216.JPG" class="lightbox" title="Lumpy pinned fabric"><img class="embedded" src="/images/creative/IMG_5216.JPG"/></a></figure>
	        
	        	<h6 class="subsection">Option C, Tie ribbons</h6>
	        	
	        	<p>Each ribbon is going in between the layers of fabric, and with just a small amount sticking out at each corner where it will be attached.</p>
	        	
	        	<figure><a href="/images/creative/Tie ribbons diagram.JPG" class="lightbox" title="Diagram showing ribbons positioned between layers of fabric"><img class="embedded" src="/images/creative/Tie ribbons diagram.JPG"/></a></figure>
	        
	        	<p>Slide one piece of ribbon in through the opening at the bottom. Then push one end of it out of the side, near the top corner. Leave just a couple of millimetres from the top (folded) edge, and about 5mm of the ribbon extending out of the side, then pin in place.</p>
	        	
	        	<figure><a href="/images/creative/IMG_5226.JPG" class="lightbox" title="End of ribbon sticking out between fabric layers"><img class="embedded" src="/images/creative/IMG_5226.JPG"/></a></figure>
	        
	        	<p>Do the same with another piece of ribbon at the other top corner. Then with a third piece, push the end of of a side near the bottom corner. Again leave 5mm of the ribbon extending out of the side, but this time leave about 15mm from the bottom edge (you're leaving space for a seam), and pin in place.</p>
	        	
	        	<figure><a href="/images/creative/IMG_5227.JPG" class="lightbox" title="End of ribbon sticking out between bottom fabric layers"><img class="embedded" src="/images/creative/IMG_5227.JPG"/></a></figure>
	        
	        	<p>Do the same thing on the other side with the last piece of ribbon. When you're done you'll have a jellyfish like this:</p>
	        	
	        	<figure><a href="/images/creative/IMG_5228.JPG" class="lightbox" title="Pinned fabric with ribbons trailing out the bottom"><img class="embedded" src="/images/creative/IMG_5228.JPG"/></a></figure>
	        
	        </section>
	        
	        <section>
	        
	        	<h4 class="section">3. Side seams</h4>
	        	
	        	<p>The rest of the construction is the same for all the options. With your fastenings pinned in place it's time to sew the sides. It just needs a straight line of plain stitches down each side, but it's a good idea to double back at the top and bottom so that the fastenings get extra reinforcement.</p>
	        	
	        	<p>So the line of stitching starts slightly below the upper fastening, goes up to the top edge, then all the way down the bottom, then back up again and ends slightly above the lower fastening.</p>
	        	
	        	<figure><a href="/images/creative/Side seam diagram.JPG" class="lightbox" title="Diagram showing line of stitching along the side of the mask"><img class="embedded" src="/images/creative/Side seam diagram.JPG"/></a></figure>
	        
	        	<p> Start a centimetre or two below the upper fastening, and sew a straight line over it until you reach the top edge. Then turn around and sew a straight line all the way back down to the bottom edge.</p>
	        	
	        	<figure><a href="/images/creative/IMG_5200.JPG" class="lightbox" title="Side seam in progress under a sewing machine foot"><img class="embedded" src="/images/creative/IMG_5200.JPG"/></a></figure>
	        
	       	 	<p>If you're making Option A the fabric might be crumpled by the elastic and difficult to keep flat in the sewing machine. Use your free hand to spread out the fabric on either side of the foot so that the part being sewn is always flat, and let the rest of the fabric take up the crumples.</p>
	        	
	        	<figure><a href="/images/creative/IMG_5201.JPG" class="lightbox" title="Hand spreading out fabric under a sewing machine foot"><img class="embedded" src="/images/creative/IMG_5201.JPG"/></a></figure>
	        	
	        	<p>Then turn once more and sew back again until a centimetre or two above the lower fastening. When you're done you'll have a straight line of stitching with two sets of loose threads, one near each of the fastenings.</p>
	        	
	        	<figure><a href="/images/creative/IMG_5203.JPG" class="lightbox" title="Finished side seam with loose threads"><img class="embedded" src="/images/creative/IMG_5203.JPG"/></a></figure>
	        
	        	<p>Do the same on the other side. Then tie off the loose threads and cut the excess off. These don't need to be sewn in because they're going to be hidden inside the finished mask.</p>
	        	
	        	<figure><a href="/images/creative/IMG_5230.JPG" class="lightbox" title="Tied and cut threads on seam"><img class="embedded" src="/images/creative/IMG_5230.JPG"/></a></figure>
	        
	        	<p>Now turn the whole thing inside-out through the bottom. Everything will hopefully look a lot more understandable now, and you'll have one of these:</p>
	        	
	        	<figure><a href="/images/creative/IMG_5231.JPG" class="lightbox" title="Inverted masks with the fastenings on the outside"><img class="embedded" src="/images/creative/IMG_5231.JPG"/></a></figure>
	        
	        </section>
	        
	        <section>
	        
	        	<h4 class="section">4. Nose wire</h4>
	        	
	        	<p>Skip this step if you don't have any wire.</p>
	        	
	        	<p>Lay the length of wire at the middle of the top edge, and mark both ends on the fabric.</p>
	        	
	        	<figure><a href="/images/creative/IMG_5233.JPG" class="lightbox" title="Strip of wire held on top of fabric"><img class="embedded" src="/images/creative/IMG_5233.JPG"/></a></figure>
	        
	        	<p>There's going to be a line of stitching along one end of the wire pocket and the whole bottom edge, but leaving a gap at the other end. For extra reinforcement it's a good idea to go over the the end twice.</p>
	        	
	        	<p>So the line of stitching starts at one bottom corner of the wire pocket, goes up, then back down, then all the way along and finishes at the other bottom corner.</p>
	        	
	        	<figure><a href="/images/creative/Wire pocket diagram.JPG" class="lightbox" title="Diagram showing line of stitching around wire pocket"><img class="embedded" src="/images/creative/Wire pocket diagram.JPG"/></a></figure>
        
        		<p>Start at the bottom corner of one of the markings for the wire, and sew a straight line up to the top edge (probably only a couple of stitches). Then turn around and sew a straight line back to the same place.</p>
        		
        		<figure><a href="/images/creative/IMG_5236.JPG" class="lightbox" title="Wire pocket stitching in progress under the sewing machine foot"><img class="embedded" src="/images/creative/IMG_5236.JPG"/></a></figure>
        
        		<p>Then sew all the way along, parallel with the top edge, until you reach the other end of the wire pocket. Stop there (don't sew back up to the top yet!). When you're done you'll have two sides of the wire pocket stitched, with loose threads coming from each lower corner.</p>
        		
        		<figure><a href="/images/creative/IMG_5238.JPG" class="lightbox" title="Stitched wire pocket with loose threads"><img class="embedded" src="/images/creative/IMG_5238.JPG"/></a></figure>
        
        		<p>Now put the wire into the pocket. Slide it in through the bottom opening, and then shuffle it into the pocket through the open edge.</p>
        		
        		<figure><a href="/images/creative/IMG_5239.JPG" class="lightbox" title="Hand sliding wire into bottom opening"><img class="embedded" src="/images/creative/IMG_5239.JPG"/></a></figure>
        
        	</section>
        	
        	<section>
        	
        		<h4 class="section">5. Bottom seam</h4>
        		
        		<p>To finish the bottom, both raw edges of fabric need to be folded inwards together. It can be fiddly, but it's easiest if you start folding in the middle and then work out towards the sides. Ironing the side seams flat first can also help (although don't do that if you used synthetic ribbon!).</p>
        		
        		<figure><a href="/images/creative/IMG_5240.JPG" class="lightbox" title="Hand folding bottom edges inwards together"><img class="embedded" src="/images/creative/IMG_5240.JPG"/></a></figure>
        		
        		<p>Aim for about 5-10mm folded over, it doesn't need to be very precise as long as it's enough to pin in place and not so much that it interferes with the fastenings. Once the edges are folded and straight, pin in place all the way along the bottom.</p>
        		
        		<figure><a href="/images/creative/IMG_5241.JPG" class="lightbox" title="Bottom edge folded and pinned"><img class="embedded" src="/images/creative/IMG_5241.JPG"/></a></figure>
        		
        		<p>Now sew a straight line all the way along. Try to stitch about 1-2mm away from the edge for the neatest result.</p>
        		
        		<figure><a href="/images/creative/IMG_5242.JPG" class="lightbox" title="Bottom edge stitched"><img class="embedded" src="/images/creative/IMG_5242.JPG"/></a></figure>
        	
			</section>
			
			<section>
			
				<h4 class="section">6. Pleats</h4>
				
				<p>Pleats are scary if you haven't done them before, but there are lots of pictures and diagrams to help! There will be three pleats on the finished mask. Some patterns use lots of repeated measuring and ironing to get them perfectly lined up, but we found it more intuitive (and much less faff) to just approximate them by hand. It's not the end of the world if they aren't perfectly centred or exactly the same size, you won't be able to tell when the mask is on anyway.</p>
				
				<p>To make each pleat, a pair of points on the fabric are brought together to touch, making a loop in between them. That loop gets flattened down so that there's a zigzag in the fabric, and then sewn over in place. If it helps, you can mark the six points on the edge of the mask before you start so that it's easier to keep track of what you're doing.</p>
				
				<figure><a href="/images/creative/Pleats diagram.JPG" class="lightbox" title="Diagram of the points to be formed into pleats along the side of the mask"><img class="embedded" src="/images/creative/Pleats diagram.JPG"/></a></figure>
			
				<p>Start with the middle pleat, it will be easier to get them all centred that way. Pinch the fabric so that the middle two points along the edge are touching.</p>
				
				<figure><a href="/images/creative/IMG_5244.JPG" class="lightbox" title="Hand pinching pleat into fabric edge"><img class="embedded" src="/images/creative/IMG_5244.JPG"/></a></figure>
			
				<p>Flatten down the loop between them so there is a flat zigzag in the fabric, and pin in place.</p>
				
				<figure><a href="/images/creative/IMG_5245.JPG" class="lightbox" title="Flattened pleat pinned into place"><img class="embedded" src="/images/creative/IMG_5245.JPG"/></a></figure>
			
				<p>Now you have one pinch in the middle of one side of the mask.</p>
				
				<figure><a href="/images/creative/IMG_5246.JPG" class="lightbox" title="Mask with a single pleat on one side"><img class="embedded" src="/images/creative/IMG_5246.JPG"/></a></figure>
				
				<p>Run that fold along to the other side of the mask, keeping it as straight as possible.</p>
				
				<figure><a href="/images/creative/IMG_5247.JPG" class="lightbox" title="Hand folding the pleat along the mask"><img class="embedded" src="/images/creative/IMG_5247.JPG"/></a></figure>
				
				<p>With the fold on the other side, flatten it and pin in place.</p>
				
				<figure><a href="/images/creative/IMG_5248.JPG" class="lightbox" title="Another pleat pinned in place"><img class="embedded" src="/images/creative/IMG_5248.JPG"/></a></figure>
				
				<p>Now grab the next pair of points on one side.</p>
				
				<figure><a href="/images/creative/IMG_5250.JPG" class="lightbox" title="Fingers pointing at edges of the mask next to a pinned pleat"><img class="embedded" src="/images/creative/IMG_5250.JPG"/></a></figure>
				
				<p>Bring the points together, making sure that the loop of excess in between goes in the same direction as the already-pinned pleat.</p>
				
				<figure><a href="/images/creative/IMG_5251.JPG" class="lightbox" title="Hand holding a loop next to an already-pinned pleat"><img class="embedded" src="/images/creative/IMG_5251.JPG"/></a></figure>
			
				<p>Flatten the loop to make a zigzag and pin in place.</p>
				
				<figure><a href="/images/creative/IMG_5252.JPG" class="lightbox" title="Second pinned pleat on one edge"><img class="embedded" src="/images/creative/IMG_5252.JPG"/></a></figure>

				<p>Now the process continues in the same way. Follow the newly-pinned fold along the mask to the other side, flatten it there and pin. Then bring the together the last pair of points on one side together.</p>
				
				<figure><a href="/images/creative/IMG_5253.JPG" class="lightbox" title="Fingers pointing at edges of the mask next to two pinned pleats"><img class="embedded" src="/images/creative/IMG_5253.JPG"/></a></figure>

				<p>Bring them together to make a loop, flatten into a zigzag, and pin. Then follow the fold along to the other side, flatten again, and pin. Each side should have three matching pleats in a row.</p>
				
				<figure><a href="/images/creative/IMG_5255.JPG" class="lightbox" title="Edge of mask showing three matching pleats"><img class="embedded" src="/images/creative/IMG_5255.JPG"/></a></figure>

				<p>And the whole thing will look roughly mask-shaped now.</p>
				
				<figure><a href="/images/creative/IMG_5256.JPG" class="lightbox" title="Mask with all pleats pinned into place"><img class="embedded" src="/images/creative/IMG_5256.JPG"/></a></figure>

				<p>Now the pleats are in place, they just need to be stitched down. Sew a straight line down each side of the mask, about 5mm from the edge, straight over the pinned folds. If you're using a machine it's easiest to sew with the folds laying down in the direction of stitching.</p>
				
				<figure><a href="/images/creative/IMG_5257.JPG" class="lightbox" title="Sewing machine stitching down over pinned pleats"><img class="embedded" src="/images/creative/IMG_5257.JPG"/></a></figure>
				
				<p>With thick fabric it might get tough to sew through all the layers of fabric on a machine, but just go slowly. If it gets stuck or bunched up, pause and lift the foot to adjust it before you carry on.</p>
				
				<figure><a href="/images/creative/IMG_5258.JPG" class="lightbox" title="Stitched-down pleats with loose threads"><img class="embedded" src="/images/creative/IMG_5258.JPG"/></a></figure>
				
				<p>Do the same thing on both sides of the mask. Once that's done, you're finished with all the main seams.</p>
				
			</section>
				
			<section>
			
				<h4 class="section">7. Finishing</h4>
				
				<p>The only thing left is to sew in the loose threads - a few stitches in place is fine. For the ends at the open side of the nose wire pocket, make sure to go upwards so that the open end gets sealed and the wire won't be able to slip out.</p>
				
				<figure><a href="/images/creative/IMG_5261.JPG" class="lightbox" title="Nose wire pocket sealed by hand"><img class="embedded" src="/images/creative/IMG_5261.JPG"/></a></figure>

				<p>Once the loose threads are dealt with, you have a finished and usable mask!<span class="dot"></span</p>
				
				<figure><a href="/images/creative/IMG_5263.JPG" class="lightbox" title="Finished masks with each type of fastening"><img class="embedded" src="/images/creative/IMG_5263.JPG"/></a></figure>

